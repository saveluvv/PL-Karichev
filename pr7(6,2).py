import math
# вводим значения длины сторон и диагонали
a = float(input("Введи длину стороны a: "))
b = float(input("Введи длину стороны b: "))
c = float(input("Введи длину стороны c: "))
d = float(input("Введи длину стороны d: "))
diagonal = float(input("Введи длину диагонали: "))
# проверяем, что введённые данные верны
if a <= 0 or b <= 0 or c <= 0 or d <= 0 or diagonal <= 0:
    print("Неверные значения. Все стороны и диагональ должны быть положительными.")
else:
# вычисляется площадь по формуле Герона
    s = (a + b + c + d) / 2  # полупериметр
    area = math.sqrt((s - a) * (s - b) * (s - c) * (s - d) - (a * c * b * d * math.cos(math.acos((a**2 + d**2 - diagonal**2) / (2 * a * d))))**2)
# результат
    print(f"Площадь четырехугольника: {area:.2f}")